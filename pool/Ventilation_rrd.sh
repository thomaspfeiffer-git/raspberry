#/bin/bash

HEARTBEAT=180

rrdtool create pool.rrd --step 60 \
DS:TEMPCPU:GAUGE:$HEARTBEAT:-40:80 \
DS:TEMPBOX:GAUGE:$HEARTBEAT:-40:80 \
DS:TEMPAIRIN:GAUGE:$HEARTBEAT:-40:80 \
DS:TEMPAIROUT:GAUGE:$HEARTBEAT:-40:80 \
DS:TEMPOUTDOOR:GAUGE:$HEARTBEAT:-40:80 \
DS:TEMPWATER:GAUGE:$HEARTBEAT:-40:80 \
DS:HUMIBOX:GAUGE:$HEARTBEAT:0:125 \
DS:HUMIAIRIN:GAUGE:$HEARTBEAT:0:125 \
DS:HUMIAIROUT:GAUGE:$HEARTBEAT:0:125 \
DS:FAN1:GAUGE:$HEARTBEAT:0:1 \
DS:FAN2:GAUGE:$HEARTBEAT:0:1 \
DS:FAN3:GAUGE:$HEARTBEAT:0:1 \
DS:FAN4:GAUGE:$HEARTBEAT:0:1 \
DS:ENGCIRC:GAUGE:$HEARTBEAT:0:5 \
DS:ENGCC:GAUGE:$HEARTBEAT:0:5 \
RRA:AVERAGE:0.5:1:7200 \
RRA:AVERAGE:0.5:5:4032 \
RRA:AVERAGE:0.5:60:744 \
RRA:AVERAGE:0.5:1440:3650 \


# 7200: 1440 Werte pro Tag x 5 Tage
# 4032: je fünf Werte (= fünf Minuten) = 12 Werte je Stunde = 288 Werte je Tag
#       = 4032 Werte für 14 Tage
# 744:  je 60 Werte (= 1 Stunde) für 31 Tage: = 24*31 = 744 Werte
# 3650: je 1440 Werte (=1 Tag) für 10 Jahre = 3650
